            <p class="ex1"><img src="img\project images\sl.jpg" alt="SAFETY LEADERSHIP" align="left"/>Safety  performance is driven by the leadership of the organization. Leaders establish values, develop procedures, and enforce accountability for their safety programs. Put another way, safety leaders set the standards of safe behavior within their companies. We would obviously expect a safety leader to be someone who exhibits high SafetyDNA, but leadership is more than just managing one’s own safety behaviors. A true safety leader also motivates his or her coworkers to strive for minimal risk exposure. First, I will discuss exactly what we mean by safety leadership and the actions performed by the employees who fill this role.Safety leadership is defined as the process of interaction between leaders and followers, through which leaders can exert their influence on followers to achieve organizational safety goals. The feature of this definition that most often confuses people is the concept of leaders and followers. Too often, the terms ‘leadership’ and ‘management’ are used interchangeably. The problem here is that many do not recognize the critical differences between these roles and the vital functions of each in building strong safety performance. Specifically, managers exist as part of the organization’s structural hierarchy and exert formal influence over their subordinates, while leadership is a voluntary activity by which an individual exerts social influence over coworkers by setting an example of appropriate behavior to elicit shared goals and effect positive change in the organization. Certainly one individual can be both a manager and a leader, but this only occurs through conscious effort to effectively perform both roles.

The misconception that safety leadership refers only to those in top management is far-reaching. For example OSHA developed the 5-STARS model of safety leadership which entails:

Supervision: overseeing work activities to make sure employees are safe

Training: conducting safety education and training

Accountability: insisting that everyone complies with company safety policies and rules

Resources: providing physical resources – tools, equipment, materials – so employees can work safely</P>
       
