<P class="ex1" scroll="auto">The major hazards associated with electricity are electrical shock, fire and arc flash.  Electrical shock occurs when the body becomes part of the electric circuit, either when an individual comes in contact with both wires of an electrical circuit, one wire of an energized circuit and the ground, or a metallic part that has become energized by contact with an electrical conductor.</p>
 <p class="ex1">
     The severity and effects of an electrical shock depend on a number of factors, such as the pathway through the body, the amount of current, the length of time of the exposure, and whether the skin is wet or dry.  Water is a great conductor of electricity, allowing current to flow more easily in wet conditions and through wet skin.</p>
 <p class="ex1">
The effect of the shock may range from a slight tingle to severe burns to cardiac arrest.  The chart below shows the general relationship between the degree of injury and amount of current for a 60-cycle hand-to-foot path of one second's duration of shock.  While reading this chart, keep in mind that most electrical circuits can provide, under normal conditions, up to 20,000 milliamperes of current flow.
     Electrical hazards can cause burns, shocks and electrocution (death).</p>
 <p class="ex1">
Assume that all overhead wires are energized at lethal voltages. Never assume that a wire is safe to touch even if it is down or appears to be insulated.
Never touch a fallen overhead power line. Call the electric utility company to report fallen electrical lines.
     Stay at least 10 feet (3 meters) away from overhead wires during cleanup and other activities. If working at heights or handling long objects, survey the area before starting work for the presence of overhead wires.</p>
 <p class="ex1">If an overhead wire falls across your vehicle while you are driving, stay inside the vehicle and continue to drive away from the line. If the engine stalls, do not leave your vehicle. Warn people not to touch the vehicle or the wire. Call or ask someone to call the local electric utility company and emergency services.
Never operate electrical equipment while you are standing in water.
Never repair electrical cords or equipment unless qualified and authorized.
Have a qualified electrician inspect electrical equipment that has gotten wet before energizing it.
If working in damp locations, inspect electric cords and equipment to ensure that they are in good condition and free of defects, and use a ground-fault circuit interrupter (GFCI).
Always use caution when working near electricity.</P>
